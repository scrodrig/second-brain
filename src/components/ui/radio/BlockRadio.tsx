"use client";

import { getColorSelectorForRadio } from "@/utils";
import { VisuallyHidden, useRadio, RadioProps } from "@nextui-org/react";

export const BlockRadio = (props: RadioProps) => {
  const { color } = props;

  const {
    Component,
    children,
    description,
    getBaseProps,
    getWrapperProps,
    getInputProps,
    getLabelProps,
    getLabelWrapperProps,
    getControlProps,
  } = useRadio(props);

  return (
    <Component {...getBaseProps()} className={getColorSelectorForRadio(color)}>
      <VisuallyHidden>
        <input {...getInputProps()} />
      </VisuallyHidden>
      <span {...getWrapperProps()}>
        <span {...getControlProps()} />
      </span>
      <div {...getLabelWrapperProps()}>
        {children && <span {...getLabelProps()}>{children}</span>}
        {description && (
          <span className="text-small text-foreground opacity-70">
            {description}
          </span>
        )}
      </div>
    </Component>
  );
};
